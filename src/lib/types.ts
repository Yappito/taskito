// ─── Graph view types ────────────────────────────────────

/** A task node positioned on the graph */
/** Minimal task info needed for graph rendering */
export interface GraphTaskData {
  id: string;
  title: string;
  priority: string;
  dueDate: Date | string;
  statusId: string;
  status: { name: string; color: string; category?: string };
  tags: Array<{ tag: { id: string; name: string; color: string } }>;
  creator?: TaskPersonSummary | null;
  assignee?: TaskPersonSummary | null;
  alertAcknowledged?: boolean;
  dependencyState?: {
    blockingTaskCount: number;
    openChildCount: number;
  };
}

export interface GraphNode {
  id: string;
  task: GraphTaskData;
  x: number;
  y: number;
  width: number;
  height: number;
}

/** A dependency edge on the graph */
export interface GraphEdge {
  id: string;
  source: string;
  target: string;
  linkType: LinkType;
  points: Array<{ x: number; y: number }>;
}

/** Result of the ELK layout computation */
export interface GraphLayout {
  nodes: GraphNode[];
  edges: GraphEdge[];
  width: number;
  height: number;
}

/** Viewport bounds for culling */
export interface Viewport {
  x: number;
  y: number;
  width: number;
  height: number;
  scale: number;
}

// ─── Search types ────────────────────────────────────────

/** Search query input */
export interface SearchInput {
  query: string;
  projectId?: string;
  filters?: {
    statusIds?: string[];
    tagIds?: string[];
    priorities?: Priority[];
    dueDateFrom?: Date;
    dueDateTo?: Date;
  };
  limit?: number;
  offset?: number;
}

/** A single search hit */
export interface SearchHit {
  task: Task;
  highlights: Record<string, string>;
}

/** Search query result */
export interface SearchResult {
  hits: SearchHit[];
  totalHits: number;
  processingTimeMs: number;
}
